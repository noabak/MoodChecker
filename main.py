
mood = input("What mood are you in today?")

if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Don't be sad!")
elif mood == "excited":
    print("Yupiiiii !")
elif mood == "relaxed":
    print("Yeaaah!")
else:
    print("I don't recognize this mood!")